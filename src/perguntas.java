    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // responde
        if(jogador.pontos==10){
        
        JOptionPane.showMessageDialog(null,"Parabens voce acertou todas!!!!");
        site.setText("https://g1.globo.com/bemestar/coronavirus/noticia/2020/02/27/coronavirus-veja-perguntas-e-respostas.ghtml");
        }
        res.getText();
        if(res.getText().equals(jogador.resposta)){
        
        jogador.pontos=jogador.pontos+2;
        quantidadePontos.setText(String.valueOf(jogador.pontos));
        JOptionPane.showMessageDialog(null,"Resposta correta + 2 ponto ");
        
 }
    else{
        JOptionPane.showMessageDialog(null,"Resposta errada");
        res.setText("");
    }
    
    }//GEN-LAST:event_jToggleButton2ActionPerformed

    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // gera
        if(jogador.numeroPerguntas<5){
            jogador.numeroPerguntas++;
        jogador.geraPergunta();
    per.setText(jogador.pergunta);
    //limpa o campo da resposta
    res.setText("");
        }
        else{
       
       JOptionPane.showMessageDialog(null,"quantidade de perguntas excedida fim do jogo!!!");
        site.setText("https://g1.globo.com/bemestar/coronavirus/noticia/2020/02/27/coronavirus-veja-perguntas-e-respostas.ghtml");
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed

